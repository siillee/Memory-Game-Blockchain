var pairOfImageIDs = ["", ""];
var pairOfImageSRCs = ["", ""];
var count = 0;

function changeImage(i) {

	if (document.getElementById("image"+i).src.match("images/faceDown.png")) {

		document.getElementById("image"+i).src = CARD_ARRAY[i];

		setTimeout(() => {

			if (pairOfImageSRCs[0] == ""){
				pairOfImageSRCs[0] = document.getElementById("image"+i).src;
				pairOfImageIDs[0] = "image"+i;
			} else {
				pairOfImageSRCs[1] = document.getElementById("image"+i).src;
				pairOfImageIDs[1] = "image"+i;

				if (pairOfImageSRCs[0] == pairOfImageSRCs[1]) {
					document.getElementById(pairOfImageIDs[0]).src = "images/blank.png";
					document.getElementById(pairOfImageIDs[1]).src = "images/blank.png";
					count++;
					if (count == 8){
						endGame();
					}
				} else {
					document.getElementById(pairOfImageIDs[0]).src = "images/faceDown.png";
					document.getElementById(pairOfImageIDs[1]).src = "images/faceDown.png";
				}
				pairOfImageSRCs[0] = "";
				pairOfImageSRCs[1] = "";
				pairOfImageIDs[0] = "";
				pairOfImageIDs[1] = "";
			}
		}, 500);	
	}


}

function endGame() {


	stopTimer();

	finishTimeInSeconds = new Date().getTime() / 1000;
	var timeMeasured = finishTimeInSeconds - startTimeInSeconds;


	if (timeMeasured <= 15) {

		GoldTokenContract.methods.mint(currAccount, "images/goldToken").send({
			from: currAccount
		});
		alert("Congratulations, you found all of them! You earned a Gold Token!");
	}else if (timeMeasured <= 20) {

		SilverTokenContract.methods.mint(currAccount, "images/silverToken").send({
			from: currAccount
		});
		alert("Congratulations, you found all of them! You earned a Silver Token!");
	}else if (timeMeasured <= 25){

		BronzeTokenContract.methods.mint(currAccount, "images/bronzeToken").send({
			from: currAccount
		});
		alert("Congratulations, you found all of them! You earned a Bronze Token!");
	} else {

		alert("Sorry, you did not win any tokens. You need to be faster!")
	}

}

